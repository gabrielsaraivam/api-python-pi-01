CREATE DATABASE dbOverall;
USE dbOverall;


CREATE TABLE tblServidor(
	id INT AUTO_INCREMENT PRIMARY KEY,
	numeroSerie INT UNIQUE KEY NOT NULL,
	latitude DECIMAL(10,8) NOT NULL,
	longitude DECIMAL(11,8) NOT NULL
);

CREATE TABLE tblDadosServidor(
	id INT AUTO_INCREMENT PRIMARY KEY,
	dataHora DATETIME NOT NULL,
	`cpu` DECIMAL(5,2) NOT NULL,
	memoria DECIMAL(5,2) NOT NULL,
	disco DECIMAL(5,2) NOT NULL,
	processosAtivos INT NOT NULL,
	usuarioAtual VARCHAR(50) NOT NULL,
	fkServidor INT,
	FOREIGN KEY (fkServidor) REFERENCES tblServidor(id)

);

